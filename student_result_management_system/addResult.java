    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        // TODO add your handling code here:
        //connecting with database
        String roll=rollNumberTextField.getText();
        String physics=physicsTextField.getText();
        String maths=mathsTextField.getText();
        String EM=EMTextField.getText();
        String DBMS=DBMSTextField.getText();
        String OS=OSTextField.getText();
        try{
            // created the driver class of sql.
            Class.forName("com.mysql.cj.jdbc.Driver");
            // calling the database.
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Kumar@6860");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+roll+"'");
            
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,physics,maths,EM,DBMS,OS) values('"+roll+"','"+physics+"','"+maths+"','"+EM+"','"+DBMS+"','"+OS+"')");
                JOptionPane.showMessageDialog(null,"Marks saved Successfully");
                setVisible(false);
                new addResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"This roll number is not registered");
            }

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_SaveButtonActionPerformed

